void show_triangle()//实时渲染波形-三角
{
    u8 x,y;
    clear(0);
    for(x=0;x<32;x++)
    {
        y=32-x;
        draw_dot(x,y);
    }
    for(x=32;x<64;x++)
    {
        y=x-32;
        draw_dot(x,y);
    }
    for(x=64;x<96;x++)
    {
        y=x-32;
        draw_dot(x,y);
    }
    for(x=96;x<128;x++)
    {
        y=159-x;
        draw_dot(x,y);
    }
}

